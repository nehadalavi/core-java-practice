public class method {
    

    // An example method
    public void printMessage() {
        System.out.println("This is simple java method!");
    }
    
    public static void main(String[] args) {
        
        // Create an instance of the class
        // containing the method
        method obj = new method();
        
        // Calling the method
        obj.printMessage(); 
    }
}

